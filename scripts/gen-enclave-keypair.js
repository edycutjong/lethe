#!/usr/bin/env node
/**
 * Generates a fresh secp256k1 keypair for the simulated enclave and prints the
 * two environment lines to paste into .env.local.
 *
 * The UI/CLI encrypt PII to the PUBLIC key; the agent decrypts with the PRIVATE
 * key, so the two MUST come from the same keypair (this script guarantees that).
 *
 * Usage:
 *   npm run gen:keys                 # print the lines
 *   npm run gen:keys >> .env.local   # append straight into your local env file
 *
 * In production the private key is sealed inside the TEE and is never generated
 * or stored on disk like this — this helper is for local/sandbox runs only.
 */
const crypto = require('crypto');

const ecdh = crypto.createECDH('secp256k1');
ecdh.generateKeys();

const privateKey = ecdh.getPrivateKey('hex');
const publicKey = ecdh.getPublicKey('hex'); // 65-byte uncompressed, starts with 04

process.stdout.write(
  `# Generated by scripts/gen-enclave-keypair.js — local/sandbox demo keypair\n` +
    `ENCLAVE_PRIVATE_KEY=${privateKey}\n` +
    `NEXT_PUBLIC_ENCLAVE_PUB_KEY=${publicKey}\n`
);
